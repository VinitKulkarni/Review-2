package com.bridgelabz.review2;

public class Contact {
    String name;
    int age;
    long phoneNumber;
    String city;
    String state;

    public Contact(String name, int age, long phoneNumber, String city,
                    String state) {
        this.name = name;
        this.age = age;
        this.phoneNumber = phoneNumber;
        this.city = city;
        this.state = state;
    }
}
